const numbers = [1, 2, 3, 4, 5];

for (let i = 0; i < numbers.length; i++) {
  console.log(numbers[i]);
}

/*
📌 Explanation:

- The loop runs once for each element in the array.
- If there are n elements → it runs n times.

👉 Time Complexity = O(n)

Meaning:
- More data → more steps (linear growth)
*/
