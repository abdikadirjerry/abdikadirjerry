/*
=====================================
02 — Comparison Operators
>, <, >=, <=, ==, ===
=====================================
*/


// ===============================
// 🟢 Easy (1–10)
// ===============================

console.log("=== EASY ===")

console.log(10 > 5)
console.log(7 < 3)
console.log(5 == 5)
console.log(10 == "10")
console.log(10 === "10")
console.log(20 >= 20)
console.log(15 <= 10)
console.log(8 != 9)
console.log("5" !== 5)
console.log(100 > 50)


// ===============================
// 🟡 Intermediate (11–20)
// ===============================

console.log("=== INTERMEDIATE ===")

let num = 120
console.log(num > 100)

let age = 19
console.log(age >= 18)

let a = 5
let b = 10
console.log(a > b)

let value = 25
console.log(value > 10 && value < 50)

let temp = -5
console.log(temp < 0)

console.log("apple" < "banana")

console.log(null == undefined)

console.log(null === undefined)

let score1 = 80
let score2 = 90
console.log(score1 > score2)

console.log(0 === 0)


// ===============================
// 🔴 Advanced (21–30)
// ===============================

console.log("=== ADVANCED ===")

let x = 30
console.log(x > 20 && x < 40)

let n1 = 15
let n2 = 20
console.log(n1 > n2 ? n1 : n2)

let p = 5, q = 5, r = 5
console.log(p === q && q === r)

console.log("hello" === "hello")

let role = "user"
console.log(role !== "admin")

console.log("100" == 100)

let password = "abc12345"
console.log(password.length >= 8)

let year = 2024
console.log(year > 2000)

console.log(a < b ? a : b)

let num2 = 150
console.log(num2 < 10 || num2 > 100)