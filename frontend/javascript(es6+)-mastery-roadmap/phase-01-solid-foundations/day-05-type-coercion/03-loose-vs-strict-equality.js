// 1. 5 == "5"
console.log(5 == "5");

// 2. 5 === "5"
console.log(5 === "5");

// 3. 0 == false
console.log(0 == false);

// 4. 0 === false
console.log(0 === false);

// 5. "" == 0
console.log("" == 0);

// 6. "" === 0
console.log("" === 0);

// 7. "10" == 10
console.log("10" == 10);

// 8. "10" === 10
console.log("10" === 10);

// 9. null == undefined
console.log(null == undefined);

// 10. null === undefined
console.log(null === undefined);

